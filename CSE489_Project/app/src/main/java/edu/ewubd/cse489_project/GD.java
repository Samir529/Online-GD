package edu.ewubd.cse489_project;

public class GD {
    String key = "";
    String datetime = "";
    String police_station = "";
    String full_name = "";
    String Nid = "";
    String address = "";
    String number = "";
    String description = "";
    String status = "";

    public GD(String key, String datetime, String police_station, String full_name, String Nid, String address,
              String number, String description, String status){
        this.key = key;
        this.datetime = datetime;
        this.police_station = police_station;
        this.full_name = full_name;
        this.Nid = Nid;
        this.address = address;
        this.number = number;
        this.description = description;
        this.status = status;

    }

}
